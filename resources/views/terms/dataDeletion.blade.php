    Política de Eliminación de Datos

Esta Política de Eliminación de Datos describe cómo gestionamos la eliminación de datos de nuestros sistemas y bases de datos.

1. Retención de Datos

Mantenemos los datos de los usuarios únicamente durante el tiempo necesario para los fines para los cuales fueron recopilados. Esto puede incluir el cumplimiento de obligaciones legales, la prestación de servicios contratados, o el tiempo necesario para los propósitos comerciales legítimos de la empresa.

2. Eliminación de Datos Personales

Cuando ya no necesitamos los datos personales para los fines descritos en nuestra Política de Privacidad, o cuando recibimos una solicitud válida de eliminación de datos por parte del usuario, procedemos a eliminarlos de nuestros sistemas de manera segura.

3. Proceso de Eliminación

La eliminación de datos se realiza de manera segura para proteger la privacidad y la confidencialidad de la información. Esto puede incluir la eliminación física o lógica de los datos de nuestros servidores y bases de datos, así como la eliminación de copias de seguridad y archivos de respaldo.

4. Excepciones

Existen ciertas situaciones en las que no podemos eliminar por completo los datos, como cuando existen obligaciones legales de retención de datos o cuando es necesario para la defensa de reclamaciones legales. En tales casos, limitamos el uso de los datos y aplicamos medidas de seguridad adicionales para proteger la privacidad de los usuarios.

5. Solicitud de Eliminación de Datos

Los usuarios tienen derecho a solicitar la eliminación de sus datos personales en cualquier momento. Para hacerlo, por favor contacta con nuestro equipo de soporte o utiliza los medios proporcionados en nuestra Política de Privacidad para presentar una solicitud de eliminación de datos.

6. Cambios en la Política

Nos reservamos el derecho de actualizar, modificar o cambiar esta Política de Eliminación de Datos en cualquier momento sin previo aviso. Se notificarán cambios significativos a través de nuestros canales de comunicación habituales.

7. Contacto

Si tienes alguna pregunta o inquietud sobre nuestra Política de Eliminación de Datos, por favor contáctanos a través de los medios proporcionados en nuestra Política de Privacidad.

Gracias por confiar en nosotros con tus datos personales. Estamos comprometidos a proteger tu privacidad y a gestionar tus datos de manera responsable.
